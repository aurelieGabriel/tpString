{
  
  data = new char[s.capacity_];
  int i;
  for(i=0; i<s.length_; i++)
    {
      data[i]= s.data[i];
    }
  length_ = s.length_;
  capacity_ = s.capacity_;
}

String::String(const char * s)
{
  capacity_ = 20;
  int i=0,l=0;
  while(s[i] != NULL)
    {
      l++;
      i++;
    }
  data = new char[capacity_];
  for(i=0;i<l;i++)
    {
      data[i] = s[i];
    }
  length_ = l;
}

